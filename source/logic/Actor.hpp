#ifndef HPP_ACTOR_ACODEMIA
#define HPP_ACTOR_ACODEMIA

////////////////////////////////////////////////////////////
// Headers
////////////////////////////////////////////////////////////
#include "Entity.hpp"
#include "../entityengine/EntityType.hpp"
#include "../entityengine/EntitySignalType.hpp"

namespace logic
{
	////////////////////////////////////////////////////////////
	/// \brief Class represents the actor
	///
	/// Inherits from the base class Entity
	///
	/// \see Entity
	///
	////////////////////////////////////////////////////////////
	class Actor : public Entity
	{
	public:

		////////////////////////////////////////////////////////////
		/// \brief Default constructor
		///
		////////////////////////////////////////////////////////////
		Actor();

		////////////////////////////////////////////////////////////
		/// \brief Copy constructor
		///
		/// \param copy - copy source
		///
		////////////////////////////////////////////////////////////
		Actor(const Actor& copy);

		////////////////////////////////////////////////////////////
		/// \brief Move constructor
		///
		/// \param other - reference to r-value 
		///
		////////////////////////////////////////////////////////////
		Actor(Actor&& other);

		////////////////////////////////////////////////////////////
		/// \brief Virtual destructor
		///
		////////////////////////////////////////////////////////////
		virtual ~Actor();

		////////////////////////////////////////////////////////////
		/// \brief Overloaded copy assignment operator
		///
		/// \param copy - copy source
		///
		////////////////////////////////////////////////////////////
		Actor& operator=(const Actor& copy);

		////////////////////////////////////////////////////////////
		/// \brief Overloaded move assignment operator
		///
		/// \param other - reference to r-value 
		///
		////////////////////////////////////////////////////////////
		Actor& operator=(Actor&& other);

		////////////////////////////////////////////////////////////
		/// \brief Set the direction vector of the object
		///
		/// \param movement - constant reference
		/// to the sf::Vector2f object
		///
		////////////////////////////////////////////////////////////
		void setMovement(const sf::Vector2f& movement);

		////////////////////////////////////////////////////////////
		/// \brief Get the velocity of the object
		///
		/// \return velocity
		///
		////////////////////////////////////////////////////////////
		const float getVelocity() const;

		////////////////////////////////////////////////////////////
		/// \brief Set the velocity of the object
		///
		/// \param velocity - velocity
		///
		////////////////////////////////////////////////////////////
		void setVelocity(float velocity);

		////////////////////////////////////////////////////////////
		/// \brief Get object destruction flag
		///
		/// \return destruction flag
		///
		////////////////////////////////////////////////////////////
		const bool getDestruction() const;

		////////////////////////////////////////////////////////////
		/// \brief Set object destruction flag
		///
		/// \param destruction - destruction flag
		///
		////////////////////////////////////////////////////////////
		void setDestruction(bool destruction);

		////////////////////////////////////////////////////////////
		/// \brief Get object health
		///
		/// \return health
		///
		////////////////////////////////////////////////////////////
		const float getHealth() const;

		////////////////////////////////////////////////////////////
		/// \brief Set object health
		///
		/// \param health - health
		///
		////////////////////////////////////////////////////////////
		void setHealth(float health);

		////////////////////////////////////////////////////////////
		/// \brief Check if the object is within the camera area
		///
		/// \return whether the object is on the camera
		///
		////////////////////////////////////////////////////////////
		const bool inCamera() const;

		////////////////////////////////////////////////////////////
		/// \brief Method returns collision damage
		///
		/// \return damage transferred at a collision
		///
		////////////////////////////////////////////////////////////
		const float getCollisionDamage() const;

		////////////////////////////////////////////////////////////
		/// \brief Method sets collision damage
		///
		/// \param collision_damage - collision damage
		///
		////////////////////////////////////////////////////////////
		void setCollisionDamage(float collision_damage);

		/////////////////////////////////////////////////////////////
		/// \brief Method updates damage
		///
		/// \param damage - damage
		///
		////////////////////////////////////////////////////////////
		void updateDamage(float damage);

		////////////////////////////////////////////////////////////
		/// \brief Method updates destruction
		///
		////////////////////////////////////////////////////////////
		void updateDestruction();

		////////////////////////////////////////////////////////////
		/// \brief Virtual method for updating an object
		///
		/// \param dt - time
		///
		////////////////////////////////////////////////////////////
		virtual void update(float dt) override;

		////////////////////////////////////////////////////////////
		/// \brief Virtual method returns the type
		/// of the object - enumeration value
		///
		/// \return Enumeration type of the object
		///
		/// \see EntityType
		///
		////////////////////////////////////////////////////////////
		virtual const EntityType getType() const;

		////////////////////////////////////////////////////////////
		/// \brief Method returns the signal
		/// generated by the object
		///
		/// \return signal generated by the object
		///
		/// \see EntitySignalType
		///
		////////////////////////////////////////////////////////////
		const EntitySignalType getSignal() const;

		////////////////////////////////////////////////////////////
		/// \brief Method set the signal type
		///
		/// \param signal - signal EntitySignalType
		///
		////////////////////////////////////////////////////////////
		void setSignal(EntitySignalType signal);

	protected:

		////////////////////////////////////////////////////////////
		// Member data
		////////////////////////////////////////////////////////////
		sf::Vector2f m_movement; // direction vector
		float m_velocity; // velocity
		bool m_destruction; // destruction flag
		float m_health; // health
		float m_collision_damage; // damage transferred at a collision
		EntitySignalType m_signal_type; // signal
			
	private:
		
	};

}//namespace logic
#endif//HPP_ACTOR_ACODEMIA
